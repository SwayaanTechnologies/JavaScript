// Math.PI: Returns the value of PI (Ï€), approximately 3.14159.
console.log(Math.PI); // Output: 3.141592653589793

// Math.abs(x): Returns the absolute (positive) value of a number x.
console.log(Math.abs(-5)); // Output: 5

// Math.ceil(x): Returns the smallest integer greater than or equal to a number x
console.log(Math.ceil(4.3)); // Output: 5

// Math.floor(x): Returns the largest integer less than or equal to a number x.
console.log(Math.floor(4.9)); // Output: 4

// Math.round(x): Rounds a number x to the nearest integer.
console.log(Math.round(4.3)); // Output: 4
console.log(Math.round(4.7)); // Output: 5

// Math.random(): Returns a pseudo-random number between 0 (inclusive) and 1 (exclusive).
console.log(Math.random()); // Output: a random number between 0 and 1

// Math.max(x1, x2, ...): Returns the largest of zero or more numbers.
console.log(Math.max(10, 20, 30)); // Output: 30

// Math.min(x1, x2, ...): Returns the smallest of zero or more numbers.
console.log(Math.min(10, 20, 30)); // Output: 10

